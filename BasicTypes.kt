// there are different types
// integers / short / long
// floats / double
// booleans - true / false
// characters - char
// strings - string


// we can declare var and use them later

// val declared without initialization
fun main() {
    val num: Int;

    // val initialized
    num = 20


    // val explicitly typed and initialized
    val g: String = "Hussein"
    println(g)
    println(num)


    val a: Int = 1000
    val b: String = "log message"
    val c: Double = 3.14
    val d: Long = 100_000_000_000_000
    val e: Boolean = false
    val f: Char = '\n'
}